import asyncHandler from "express-async-handler";
import Comment from "../../model/comment/Comment.js";
import { blockUsers } from "../../utils/blockUser.js";
import { validateMongodbId } from "../../utils/validateMongodbID.js";

//Create a Comment
export const createComment = asyncHandler(async (req, res) => {
  //1.Get the user
  const user = req.user;
  //Check if user is blocked
  blockUsers(user);
  //2.Get the post Id
  const { postId, description } = req.body;

  try {
    const comment = await Comment.create({
      post: postId,
      user,
      description,
    });
    res.json(comment);
  } catch (error) {
    res.json(error);
  }
});

//fetch all comments
export const fetchAllComments = asyncHandler(async (req, res) => {
  try {
    const comments = await Comment.find({}).sort("-created");
    res.json(comments);
  } catch (error) {
    res.json(error);
  }
});

//fetch or get  single comment
export const fetchComment = asyncHandler(async (req, res) => {
  const { id } = req.params;
  validateMongodbId(id);
  try {
    const comment = await Comment.findById(id);
    res.json(comment);
  } catch (error) {
    res.json(error);
  }
});

//Update comment
export const updateComment = asyncHandler(async (req, res) => {
  const { id } = req.params;
  validateMongodbId(id);
  try {
    const update = await Comment.findByIdAndUpdate(
      id,
      {
        user: req?.user,
        description: req?.body?.description,
      },
      {
        new: true,
        runValidators: true,
      }
    );
    res.json(update);
  } catch (error) {
    res.json(error);
  }
});

//delete comment
export const deleteComment = asyncHandler(async (req, res) => {
  const { id } = req.params;
  validateMongodbId(id);
  try {
    const comment = await Comment.findByIdAndDelete(id);
    res.json(comment);
  } catch (error) {
    res.json(error);
  }
});
